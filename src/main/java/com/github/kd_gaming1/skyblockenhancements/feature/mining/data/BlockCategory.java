package com.github.kd_gaming1.skyblockenhancements.feature.mining.data;

/**
 * Classifies mineable blocks for instant-mine multiplier and filter toggles.
 */
public enum BlockCategory {
    STANDARD,    // Hard Stone, Obsidian, Netherrack, Stone, etc.
    ORE,         // Vanilla ores, Titanium, Sulphur, Tungsten, Umber, Glacite
    PURE_ORE,    // Pure variants in Crystal Hollows
    MITHRIL,     // Gray/Green/Blue Mithril
    GEMSTONE     // All 12 gemstone types
}
